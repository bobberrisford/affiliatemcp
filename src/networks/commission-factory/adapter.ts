// Domain object transformers
// ---------------------------------------------------------------------------

/**
 * Compute the age (in days) of a Commission Factory transaction at the moment
 * the adapter responded. The unpaid-age affordance depends on this.
 *
 * Anchor priority: OrderDate (the conversion date) is the most meaningful and
 * earliest anchor; fall back to DateCreated (when CF recorded the transaction).
 */
function computeAgeDays(raw: CommissionFactoryTransactionRaw, now: Date = new Date()): number {
  const anchor = raw.OrderDate ?? raw.DateCreated;
  if (!anchor) return 0;
  const t = Date.parse(anchor);
  if (Number.isNaN(t)) return 0;
  const ms = now.getTime() - t;
  return Math.max(0, Math.floor(ms / (1000 * 60 * 60 * 24)));
}

function nullableIso(d?: string | null): string | undefined {
  if (!d) return undefined;
  const ts = Date.parse(d);
  return Number.isNaN(ts) ? undefined : new Date(ts).toISOString();
}

function toAmount(v: number | string | undefined): number {
  if (v === undefined || v === null) return 0;
  const n = typeof v === 'number' ? v : parseFloat(String(v));
  return Number.isNaN(n) ? 0 : n;
}

function toTransaction(
  raw: CommissionFactoryTransactionRaw,
  now: Date = new Date(),
): Transaction {
  const status = mapTransactionStatus(raw);
  const commission = toAmount(raw.Commission);
  const sale = toAmount(raw.SaleValue ?? raw.ReportedSaleValue);
  // Currency is read per transaction — Commission Factory is APAC-centric (often
  // AUD) but we never hardcode; default only when the field is genuinely absent.
  const currency = (raw.ReportedCurrencyCode ?? 'AUD').toUpperCase();

  const orderDate = nullableIso(raw.OrderDate) ?? nullableIso(raw.DateCreated) ?? new Date(0).toISOString();
  const createdDate = nullableIso(raw.DateCreated);
  // Commission Factory does not expose an approval timestamp distinct from
  // DateModified; we surface DateModified as the approved date only when the
  // transaction is approved/paid, otherwise leave it undefined.
  const approvedDate =
    status === 'approved' || status === 'paid' ? nullableIso(raw.DateModified) : undefined;
  const paidDate = status === 'paid' ? nullableIso(raw.DateModified) : undefined;

  return {
    id: String(raw.Id ?? ''),
    network: SLUG,
    programmeId: String(raw.MerchantId ?? ''),
    programmeName: raw.MerchantName ?? `Commission Factory merchant ${raw.MerchantId ?? ''}`,
    status,
    amount: sale,
    currency,
    commission,
    dateClicked: createdDate,
    dateConverted: orderDate,
    dateApproved: approvedDate,
    datePaid: paidDate,
    ageDays: computeAgeDays(raw, now),
    reversalReason: status === 'reversed' ? raw.VoidReason ?? undefined : undefined,
    rawNetworkData: raw,
  };
}

function toProgramme(raw: CommissionFactoryMerchantRaw): Programme {
  const currency = (raw.CurrencyCode ?? raw.Currency)?.toUpperCase();
  const rate = mapCommissionRate(raw);
  const programme: Programme = {
    id: String(raw.Id ?? ''),
    name: raw.Name ?? `Commission Factory merchant ${raw.Id ?? ''}`,
    network: SLUG,
    status: mapProgrammeStatus(raw),
    rawNetworkData: raw,
  };
  if (currency) programme.currency = currency;
  if (rate) programme.commissionRate = rate;
  if (raw.TargetUrl) programme.advertiserUrl = raw.TargetUrl;
  return programme;
}

