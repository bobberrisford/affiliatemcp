/**
 * Commission Factory functions return either a bare JSON array or, in some
 * documented examples, an object wrapping the array (e.g. `{ Items: [...] }`).
 * Normalise both to a plain array so transformers do not have to guess.
 */
function asArray<T>(response: unknown): T[] {
  if (Array.isArray(response)) return response as T[];
  if (response && typeof response === 'object') {
    const obj = response as Record<string, unknown>;
    for (const key of ['Items', 'Data', 'Results', 'Transactions', 'Merchants']) {
      if (Array.isArray(obj[key])) return obj[key] as T[];
    }
  }
  return [];
}

/**
 * Build a Commission Factory deep link from a merchant TrackingUrl and a
 * destination URL.
 *
 * The merchant TrackingUrl is a text-link host like `https://t.cfjump.com/0/t/{id}`.
 * Deep links use the banner/deep-link segment `.../b/{id}` with the destination
 * passed as the `Url` query parameter.
 */
function buildDeepLink(trackingUrl: string, destinationUrl: string): string {
  // Swap the `/t/` text-link segment for the `/b/` deep-link segment where present.
  const deepBase = trackingUrl.includes('/t/')
    ? trackingUrl.replace('/t/', '/b/')
    : trackingUrl;
  const sep = deepBase.includes('?') ? '&' : '?';
  return `${deepBase}${sep}Url=${encodeURIComponent(destinationUrl)}`;
}

