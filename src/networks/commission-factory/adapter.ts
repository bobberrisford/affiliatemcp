/**
 * Map a set of canonical TransactionStatus values to a single Commission Factory
 * Status2 value. Returns undefined if the set requires client-side filtering
 * (multiple statuses, or a status with no single upstream equivalent).
 *
 * Canonical → CF Status2:
 *   pending   → 'Pending'
 *   approved  → 'Confirmed'
 *   paid      → 'Paid'
 *   reversed  → (no single value; CF splits into Declined + Void) → undefined
 *   other     → (no mapping) → undefined
 */
function mapCanonicalToCfStatus(statuses?: TransactionStatus[]): string | undefined {
  if (!statuses || statuses.length !== 1) return undefined;
  switch (statuses[0]) {
    case 'pending':
      return 'Pending';
    case 'approved':
      return 'Confirmed';
    case 'paid':
      return 'Paid';
    default:
      // 'reversed' maps to two upstream values (Declined, Void) so we cannot
      // express it as a single server filter; 'other' has no equivalent.
      return undefined;
  }
}

