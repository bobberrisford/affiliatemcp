// Why deliberately minimal: Commission Factory documents field names but the set
// can vary across functions and account types. Treating every field as possibly
// absent and preserving the original under `rawNetworkData` keeps the adapter
// robust to upstream drift.

interface CommissionFactoryTransactionRaw {
  Id?: string | number;
  DateCreated?: string; // ISO 8601 — when CF recorded the transaction
  DateModified?: string; // ISO 8601
  OrderDate?: string; // ISO 8601 — when the order was placed (conversion)
  OrderId?: string;
  MerchantId?: string | number;
  MerchantName?: string;
  ReportedCurrencyCode?: string; // e.g. "AUD"
  ReportedSaleValue?: number | string;
  SaleValue?: number | string; // gross sale value
  Commission?: number | string; // commission to the affiliate
  Status?: string; // deprecated TransactionStatus enum (fallback)
  Status2?: string; // TransactionStatus2: Pending | Confirmed | Declined | Void | Paid
  CouponCode?: string;
  UniqueId?: string;
  VoidReason?: string; // set when the transaction is voided/declined
}

interface CommissionFactoryMerchantRaw {
  Id?: string | number;
  Name?: string;
  AvatarUrl?: string;
  CommissionType?: string; // e.g. "Percentage", "Fixed", "Percent per Sale"
  CommissionRate?: number | string;
  Status?: string; // MerchantStatus: Joined | Pending | Declined | Suspended | ...
  TrackingUrl?: string; // e.g. https://t.cfjump.com/0/t/{id}
  TargetUrl?: string; // the merchant's destination site
  Currency?: string;
  CurrencyCode?: string;
}

// ---------------------------------------------------------------------------
// Status mapping helpers
// ---------------------------------------------------------------------------

/**
 * Map a Commission Factory transaction status to the canonical TransactionStatus.
 *
 * Commission Factory's current enum is `TransactionStatus2`; the older
 * `TransactionStatus` is deprecated. We read Status2 first and fall back to Status.
 *
 *   Pending             → 'pending'   (awaiting validation)
 *   Confirmed           → 'approved'  (validated, not yet paid out)
 *   Paid                → 'paid'      (settled / included in an invoice)
 *   Declined / Void     → 'reversed'  (the sale did not pay out)
 *   anything else       → 'other'
 *
 * Why 'Declined'/'Void' → 'reversed': from the publisher's perspective both mean
 * the sale did not pay out — semantically a reversal, matching what other
 * networks call this state. The verbatim status is preserved in `rawNetworkData`.
 */
function mapTransactionStatus(raw: CommissionFactoryTransactionRaw): TransactionStatus {
  const s = (raw.Status2 ?? raw.Status ?? '').toLowerCase().trim();
  if (s === 'pending') return 'pending';
  if (s === 'confirmed' || s === 'approved' || s === 'accepted') return 'approved';
  if (s === 'paid') return 'paid';
  if (s === 'declined' || s === 'void' || s === 'voided' || s === 'reversed' || s === 'rejected') {
    return 'reversed';
  }
  return 'other';
}

/**
 * Map a Commission Factory merchant status to the canonical ProgrammeStatus.
 *
 *   Joined / Active       → 'joined'
 *   Pending               → 'pending'
 *   Declined / Rejected   → 'declined'
 *   Available / NotJoined → 'available'
 *   Suspended / Paused    → 'suspended'
 *   anything else         → 'unknown'
 */
function mapProgrammeStatus(raw: { Status?: string }): ProgrammeStatus {
  const s = (raw.Status ?? '').toLowerCase().trim();
  if (s === 'joined' || s === 'active' || s === 'approved') return 'joined';
  if (s === 'pending') return 'pending';
  if (s === 'declined' || s === 'rejected') return 'declined';
  if (s === 'available' || s === 'notjoined' || s === 'not joined') return 'available';
  if (s === 'suspended' || s === 'paused') return 'suspended';
  return 'unknown';
}

/**
 * Map a Commission Factory CommissionType/CommissionRate pair to the structured
 * commission rate shape. We pick the closest of the canonical types and preserve
 * the verbatim source in `rawNetworkData` on the Programme.
 */
function mapCommissionRate(raw: CommissionFactoryMerchantRaw): CommissionRateStructured | undefined {
  if (raw.CommissionType === undefined && raw.CommissionRate === undefined) return undefined;
  const t = (raw.CommissionType ?? '').toLowerCase();
  const value = toAmount(raw.CommissionRate);
  let type: CommissionRateStructured['type'] = 'unknown';
  if (t.includes('percent')) type = 'percent';
  else if (t.includes('fixed') || t.includes('flat') || t.includes('amount')) type = 'flat';
  const out: CommissionRateStructured = { type };
  if (raw.CommissionRate !== undefined) out.value = value;
  if (raw.CommissionType) out.description = raw.CommissionType;
  return out;
}

